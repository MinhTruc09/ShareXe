class HomePscreen extends StatefulWidget {
  const HomePscreen({super.key});

  @override
  State<HomePscreen> createState() => _HomePscreenState();
}

class _HomePscreenState extends State<HomePscreen> {
  final RideService _rideService = RideService();
  final AuthService _authService = AuthService();
  late AuthController _authController;
  int _currentIndex = 0;

  String _departure = '';
  String _destination = '';
  DateTime? _departureDate;
  int _passengerCount = 1;

  List<Ride> _availableRides = [];
  bool _isLoading = false;
  bool _isRefreshing = false;

  @override
  void initState() {
    super.initState();
    _authController = AuthController(AuthService());
    _fetchAvailableRides();
  }

  @override
  void dispose() {
    super.dispose();
  }

  Future<void> _fetchAvailableRides() async {
    setState(() {
      _isLoading = true;
    });

    try {
      print('üîç Fetching available rides from API...');
      // Fetching available rides from the specific endpoint
      final rides = await _rideService.getAvailableRides();

      print('‚úÖ Successfully fetched ${rides.length} rides from API');

      if (mounted) {
        setState(() {
          _availableRides = rides;
          _isLoading = false;
        });

        if (rides.isEmpty) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(content: Text('Kh√¥ng c√≥ chuy·∫øn xe ph√π h·ª£p')),
          );
        }
      }
    } catch (e) {
      print('‚ùå Error fetching rides: $e');
      if (mounted) {
        setState(() {
          _isLoading = false;
          // Set empty list on error
          _availableRides = [];
        });
        // Show error message
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Kh√¥ng th·ªÉ t·∫£i danh s√°ch chuy·∫øn xe: $e')),
        );
      }
    }
  }

  Future<void> _refreshRides() async {
    if (_isRefreshing) return;

    setState(() {
      _isRefreshing = true;
    });

    try {
      print('üîÑ Refreshing available rides from API...');
      final rides = await _rideService.getAvailableRides();

      print('‚úÖ Successfully refreshed ${rides.length} rides from API');

      if (mounted) {
        setState(() {
          _availableRides = rides;
        });

        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('ƒê√£ c·∫≠p nh·∫≠t danh s√°ch chuy·∫øn xe')),
        );
      }
    } catch (e) {
      print('‚ùå Error refreshing rides: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Kh√¥ng th·ªÉ c·∫≠p nh·∫≠t danh s√°ch: $e')),
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isRefreshing = false;
        });
      }
    }
  }

  Future<void> _searchRides() async {
    if (_departure.isEmpty && _destination.isEmpty && _departureDate == null) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Vui l√≤ng nh·∫≠p √≠t nh·∫•t m·ªôt ƒëi·ªÅu ki·ªán t√¨m ki·∫øm'),
        ),
      );
      return;
    }

    setState(() {
      _isLoading = true;
    });

    try {
      final rides = await _rideService.searchRides(
        departure: _departure,
        destination: _destination,
        startTime: _departureDate,
        passengerCount: _passengerCount,
      );

      setState(() {
        _availableRides = rides;
        _isLoading = false;
      });

      if (rides.isEmpty) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Kh√¥ng t√¨m th·∫•y chuy·∫øn xe ph√π h·ª£p')),
        );
      } else {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('T√¨m th·∫•y ${rides.length} chuy·∫øn ƒëi ph√π h·ª£p')),
        );
      }
    } catch (e) {
      setState(() {
        _isLoading = false;
      });
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text('L·ªói t√¨m ki·∫øm: $e')));
    }
  }

  void _logout() async {
    try {
      await _authController.logout();
      if (mounted) {
        Navigator.pushReplacementNamed(context, AppRoute.role);
      }
    } catch (e) {
      print(e);
    }
  }

  void _onBottomNavTap(int index) {
    setState(() {
      _currentIndex = index;
    });
  }

  Widget _buildBody() {
    switch (_currentIndex) {
      case 0:
        return _buildHomeContent();
      case 1:
        return const Center(child: Text('Trang Chuy·∫øn ƒëi'));
      case 2:
        return const ChatListScreen();
      case 3:
        return const ProfileScreen();
      default:
        return _buildHomeContent();
    }
  }

  Widget _buildHomeContent() {
    return SingleChildScrollView(
      child: Column(
        children: [
          Padding(
            padding: const EdgeInsets.fromLTRB(16, 16, 16, 8),
            child: RideSearchCard(
              onSearch: (departure, destination, date) {
                setState(() {
                  _departure = departure;
                  _destination = destination;
                  _departureDate = date;
                });
                _searchRides();
              },
            ),
          ),
          const Padding(
            padding: EdgeInsets.symmetric(horizontal: 16),
            child: RecentSearches(),
          ),
          // Display available rides
          if (_isLoading)
            const Center(
              child: Padding(
                padding: EdgeInsets.all(20.0),
                child: CircularProgressIndicator(),
              ),
            )
          else
            Padding(
              padding: const EdgeInsets.all(16.0),
              child: _buildRidesList(),
            ),
        ],
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    return SharexeBackground2(
      child: Scaffold(
        backgroundColor: Colors.transparent,
        appBar: AppBar(
          title: const Text('ShareXE'),
          backgroundColor: const Color(0xFF00AEEF),
          actions: [
            IconButton(
              icon: const Icon(Icons.logout),
              onPressed: _logout,
            ),
          ],
        ),
        body: _buildBody(),
        bottomNavigationBar: BottomNavigationBar(
          type: BottomNavigationBarType.fixed,
          backgroundColor: Colors.white,
          selectedItemColor: const Color(0xFF00AEEF),
          unselectedItemColor: Colors.grey,
          currentIndex: _currentIndex,
          onTap: _onBottomNavTap,
          items: const [
            BottomNavigationBarItem(icon: Icon(Icons.home), label: 'Trang ch·ªß'),
            BottomNavigationBarItem(
              icon: Icon(Icons.history),
              label: 'Chuy·∫øn ƒëi',
            ),
            BottomNavigationBarItem(icon: Icon(Icons.message), label: 'Tin nh·∫Øn'),
            BottomNavigationBarItem(icon: Icon(Icons.person), label: 'C√° nh√¢n'),
          ],
        ),
      ),
    );
  }

  Widget _buildSearchCard() {
    return Card(
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      color: Colors.white,
      child: Padding(
        padding: const EdgeInsets.all(16.0),
        child: Column(
          children: [
            LocationPicker(
              title: 'ƒêi·ªÉm ƒëi',
              icon: Icons.circle_outlined,
              hintText: 'Xu·∫•t ph√°t t·ª´',
              onLocationSelected: (location) {
                setState(() {
                  _departure = location;
                });
              },
            ),
            const Divider(height: 1),
            LocationPicker(
              title: 'ƒêi·ªÉm ƒë·∫øn',
              icon: Icons.location_on_outlined,
              hintText: 'ƒêi·ªÉm ƒë·∫øn',
              onLocationSelected: (location) {
                setState(() {
                  _destination = location;
                });
              },
            ),
            const Divider(height: 1),
            DatePickerField(
              icon: Icons.access_time,
              hintText: 'Th·ªùi gian xu·∫•t ph√°t',
              onDateSelected: (date) {
                setState(() {
                  _departureDate = date;
                });
              },
            ),
            const Divider(height: 1),
            PassengerCounter(
              icon: Icons.people_outline,
              hintText: 'S·ªë l∆∞·ª£ng',
              onCountChanged: (count) {
                setState(() {
                  _passengerCount = count;
                });
              },
            ),
            const SizedBox(height: 16),
            SizedBox(
              width: double.infinity,
              child: ElevatedButton(
                onPressed: _searchRides,
                style: ElevatedButton.styleFrom(
                  backgroundColor: const Color(0xFF002D62),
                  foregroundColor: Colors.white,
                  shape: RoundedRectangleBorder(
                    borderRadius: BorderRadius.circular(30),
                  ),
                  padding: const EdgeInsets.symmetric(vertical: 12),
                ),
                child: const Text('T√¨m chuy·∫øn', style: TextStyle(fontSize: 16)),
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildRidesList() {
    if (_availableRides.isEmpty) {
      return const Center(
        child: Padding(
          padding: EdgeInsets.all(20.0),
          child: Text(
            'Kh√¥ng c√≥ chuy·∫øn xe n√†o ph√π h·ª£p v·ªõi t√¨m ki·∫øm c·ªßa b·∫°n',
            style: TextStyle(color: Colors.white, fontSize: 16),
            textAlign: TextAlign.center,
          ),
        ),
      );
    }

    return ListView.builder(
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      itemCount: _availableRides.length,
      itemBuilder: (context, index) {
        try {
          return RideCard(
            ride: _availableRides[index],
            onTap: () async {
              // Load ride details when tapped
              final rideId = _availableRides[index].id;
              final rideDetails = await _rideService.getRideDetails(rideId);

              if (mounted && rideDetails != null) {
                // Show ride details (you'd have a screen for this)
                Navigator.pushNamed(
                  context,
                  AppRoute.rideDetails,
                  arguments: rideDetails,
                );
              }
            },
          );
        } catch (e) {
          // Handle any rendering errors for individual cards
          print('Error rendering ride card at index $index: $e');
          return Card(
            margin: const EdgeInsets.symmetric(vertical: 8),
            color: Colors.red.shade100,
            child: Padding(
              padding: const EdgeInsets.all(16.0),
              child: Text(
                'L·ªói hi·ªÉn th·ªã chuy·∫øn xe: $e',
                style: const TextStyle(color: Colors.red),
              ),
            ),
          );
        }
      },
    );
  }
}
